#include<bits/stdc++.h>
using namespace std;

int main()
{
     // Uppercase Alphabet 
    for ( char c='A' ; c<='Z+1' ; c++)
    {
        cout << c << " " ;
    }
    cout << endl ;

    // Lower case Alphabet
    for ( char c='a' ; c<='z' ; c++)
    {
        cout << c << " " ;
    }

  return 0;
}